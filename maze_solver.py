def solve_maze_bfs(maze, start, end):
    rows, cols = len(maze), len(maze[0])
    queue = collections.deque([(start, [start])])
    visited = set([start])

    while queue:
        (r, c), path = queue.popleft()

        if (r, c) == end:
            return path

        for dr, dc in [(1,0),(-1,0),(0,1),(0,-1)]:
            nr, nc = r + dr, c + dc
            if (
                0 <= nr < rows and 0 <= nc < cols and
                maze[nr][nc] == 0 and
                (nr, nc) not in visited
            ):
                visited.add((nr, nc))
                queue.append(((nr, nc), path + [(nr, nc)]))

    return None

def solve_maze_dfs(maze, start, end):
    rows, cols = len(maze), len(maze[0])
    stack = [(start, [start])]
    visited = set()

    while stack:
        (r, c), path = stack.pop()

        if (r, c) == end:
            return path

        if (r, c) in visited:
            continue
        visited.add((r, c))

        for dr, dc in [(1,0),(-1,0),(0,1),(0,-1)]:
            nr, nc = r + dr, c + dc
            if (
                0 <= nr < rows and 0 <= nc < cols and
                maze[nr][nc] == 0 and
                (nr, nc) not in visited
            ):
                stack.append(((nr, nc), path + [(nr, nc)]))

    return None

def solve_maze_a_star(maze, start, end):
    rows, cols = len(maze), len(maze[0])

    def h(a, b):
        return abs(a[0] - b[0]) + abs(a[1] - b[1])

    pq = [(h(start, end), 0, start, [start])]
    best = {start: 0}
    visited = set()

    while pq:
        f, g, (r, c), path = heapq.heappop(pq)

        if (r, c) == end:
            return path

        if (r, c) in visited:
            continue
        visited.add((r, c))

        for dr, dc in [(1,0),(-1,0),(0,1),(0,-1)]:
            nr, nc = r + dr, c + dc
            if not (0 <= nr < rows and 0 <= nc < cols): continue
            if maze[nr][nc] == 1: continue

            ng = g + 1
            if ng < best.get((nr, nc), 9999999):
                best[(nr, nc)] = ng
                heapq.heappush(pq, (ng + h((nr, nc), end), ng, (nr, nc), path + [(nr, nc)]))

    return None
